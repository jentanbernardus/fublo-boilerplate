<h1>Welcome to Fublo Boilerplate</h1>
<h2>You activated this bare-bones WordPress theme!</h2>

<h3>For Developers</h3>
<p>The following are good next steps if you want to use Fublo
Boilerplate in your theme development.</p>
<ol>
    <li>You should create a child theme that uses this as its
    parent. <a href=''>Example here!!!</a></li>
    <li>Activate your new child theme and keep the boilerplate
    deactivated, but still installed in your WordPress themes directory.
    More info about <a href="https://codex.wordpress.org/Child_Themes">child
    themes on WordPress Codex</a>.</li>
    <li>Currently Fublo Boilerplate only resides on
    <a href="https://github.com/fublo/fublo-boilerplate/">GitHub</a>. You
    can keep it updated using the Github updater, which also works for
    other plugins and themes that use GitHub for their code hosting,
    such as <a href='https://github.com/scribu/wp-posts-to-posts'> Posts
    2 Posts</a></li>
</ol>
<p>You can easily start a child theme by including boilerplate as the
parent with the following in your style.css:</p>
<p>Template: fublo-boilerplate</p>

<h3>For Site Admins</h3>
<p>This is not a ready-to-publish WordPress theme.</p>
<p>Instead it's a theme framework that combines HTML and CSS resources
into one package with the goal of creating a route for quick theme
deployment for developers.</p>
<p>If you're cleaning out a WordPress install, ensure that this theme is
not used by any of your active themes before removing the theme
folder.</p>
<p>Information about developing on the boilerplate can be found on
<a href="http://fublo.github.com/fublo-boilerplate/">GitHub</a>.</p>

